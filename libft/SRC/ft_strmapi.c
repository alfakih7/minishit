#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	n;
	char	*d;

	if (!s)
		return (NULL);
	i = 0;
	n = ft_strlen(s);
	d = (char *)malloc((n + 1) * sizeof (char));
	if (!d)
		return (NULL);
	while (s[i])
	{
		d[i] = f(i, s[i]);
		i++;
	}
	d[i] = '\0';
	return (d);
}
